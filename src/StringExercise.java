import java.util.Scanner;

public class StringExercise {
    public static void main(String[] args) {
        String output = "We don't need no education\n" +
                "We don't need no thought control\n";
        System.out.println (output);

        String checkout = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(checkout);

        String windows = "In windows, the main drive is usually C:\\\n";
        System.out.println(windows);

        String backslashes = """
                I can do backslashes \\, double backslashes \\\\,
                and the amazing triple backslash \\\\\\!
                """;
        System.out.println(backslashes);

    }
}


